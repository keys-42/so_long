#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	len;
	size_t	i;

	i = 0;
	len = ft_strlen(src);
	if (dstsize == 0)
		return (len);
	while (*src != '\0' && i++ < dstsize - 1)
	{
		*(unsigned char *)dst++ = *(unsigned char *)src++;
	}
	*(unsigned char *)dst = '\0';
	return (len);
}
